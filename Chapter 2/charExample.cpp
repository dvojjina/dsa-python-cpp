#include <iostream>
using namespace std;

int main(){
    char letter = 'A';
    char symbol = '#';
    char digit = '9';

    cout << "The character stored in letter: " << letter << endl;
    cout << "The ASCII value stored in letter: " << int(letter) << endl;
    cout << "The character stored in symbol: " << symbol << endl;
    cout << "The ASCII value stored in symbol: " << int(symbol) << endl;
    cout << "The character stored in digit: " << digit << endl;
    cout << "The ASCII value stored in digit: " << int(digit) << endl;

    return 0;

}